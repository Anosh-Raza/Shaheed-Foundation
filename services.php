<script>
// Service section navigation
document.addEventListener('DOMContentLoaded', function() {
    const navTabs = document.querySelectorAll('.nav-tab');
    const serviceSections = document.querySelectorAll('.service-section');

    navTabs.forEach(tab => {
        tab.addEventListener('click', function(e) {
            e.preventDefault();
            
            // Remove active class from all tabs and sections
            navTabs.forEach(t => t.classList.remove('active'));
            serviceSections.forEach(s => s.classList.remove('active'));
            
            // Add active class to clicked tab
            this.classList.add('active');
            
            // Show corresponding section
            const targetSection = this.dataset.section;
            const section = document.getElementById(targetSection);
            if (section) {
                section.classList.add('active');
            }
        });
    });

    // Appointment form submission
    const appointmentForm = document.querySelector('.appointment-request');
    appointmentForm.addEventListener('submit', function(e) {
        e.preventDefault();
        
        // Get form data
        const formData = new FormData(this);
        const patientName = document.getElementById('patientName').value;
        const patientPhone = document.getElementById('patientPhone').value;
        const serviceType = document.getElementById('serviceType').value;
        
        if (!patientName || !patientPhone || !serviceType) {
            alert('Please fill in all required fields.');
            return;
        }
        
        // Here you would typically send the data to a server
        alert('Thank you! Your appointment request has been submitted. We will contact you soon to confirm your appointment.');
        
        // Reset form
        this.reset();
    });
});
</script>

